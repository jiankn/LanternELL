## Why Visual Supports Work for ELL Students

The science behind visual supports is straightforward: the brain processes images faster than text, and visual memory is stronger than auditory memory. For ELL students who are still decoding English, visual supports provide an alternative pathway to understanding.

During the **silent period** — the weeks or months when newcomer students are absorbing English but not yet producing it — visual supports are often the only way students can access classroom content. A student who cannot yet read "Put your materials away" can follow a visual schedule card showing a picture of a student putting books in a bin.

Visual supports also reduce **cognitive load**. When a student doesn''t have to spend mental energy decoding every English word, they can focus on understanding the actual content. This is why ELL students often perform better on assessments that include visual supports than on text-only assessments.

