## Activity 2: Classroom Tour with Visual Supports (15 minutes)

Walk students through the classroom, stopping at each labeled area. Point to the label, say the English word clearly, and have students repeat. Touch the object as you name it — this multi-sensory approach helps words stick.

Key stops: door, window, desk, chair, board, bathroom, water fountain, trash can. These are the [visual supports for ELL](/visual-supports-ell) survival vocabulary words that students will use every day.

**Give each student a mini-map** of the classroom with pictures. As you visit each area, students can check it off on their map.

## Activity 3: All About Me Poster (20 minutes)

Give each student a simple poster template with sections for: My Name, My Country (draw a flag), My Family (draw), My Favorite Food (draw), My Favorite Color (color a circle). No English writing required — just drawing and coloring.

This activity is low-stress, creative, and produces a product that can be displayed on the wall. It also gives you valuable information about each student''s background.

**Bilingual option:** Include labels in both English and the student''s home language. Our [English-Spanish printables](/english-spanish-printables) include "All About Me" templates in bilingual format.

## Activity 4: Survival Vocabulary Cards (15 minutes)

Introduce 5 essential words with picture cards: bathroom, water, help, yes, no. Hold up each card, say the word, and have students repeat. Then practice in context:

- Walk to the bathroom, hold up the card, say "bathroom"
- Go to the water fountain, hold up the card, say "water"
- Raise your hand, hold up the card, say "help"

Give each student their own set of cards on a ring to keep at their desk. These become their communication lifeline for the first weeks. Our [newcomer activities](/newcomer-activities) collection includes printable survival vocabulary card sets.

## Activity 5: Color Sorting Game (10 minutes)

Place colored objects (blocks, crayons, paper squares) on a table. Hold up a red object and say "red." Have students find all the red objects and place them together. Repeat with blue, green, yellow.

**Why it works for beginners:** Colors are concrete, visual, and immediately useful. Students can participate fully without any prior English knowledge. And they leave day one knowing 4-6 English words.

## Activity 6: Goodbye Routine (5 minutes)

End the day with a consistent goodbye routine that you will repeat every day. Stand at the door, make eye contact with each student, and say "Goodbye, [name]. See you tomorrow." Teach students to respond "Goodbye" or simply wave.

Consistency in the goodbye routine gives students something predictable to look forward to and a sense of closure that reduces anxiety about returning the next day.

## What NOT to Do on the First Day

- Don''t give a written assessment or placement test
- Don''t expect students to read or write in English
- Don''t speak too fast or use complex sentences
- Don''t single out students who are quiet — the silent period is normal
- Don''t skip the visual supports — they are not optional for beginners

