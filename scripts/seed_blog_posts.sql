## How to Implement Sentence Frames

### Display Them Visibly
Post sentence frames on anchor charts, at writing centers, and on student desks. Make them easy to reference during independent work.

### Model Their Use
Before asking students to use a frame independently, model it yourself. Think aloud as you fill in the blanks, showing students your thought process.

### Practice Orally First
Have students practice sentence frames in speaking before writing. Partner discussions using frames build fluency and confidence before the added challenge of writing.

### Gradually Release
As students internalize sentence patterns, fade the scaffolding. Move from full frames to sentence starters to independent writing. The goal is for students to eventually produce these structures without the frame.

